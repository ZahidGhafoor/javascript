/*
    DO-WHILE LOOP QUIZ:
        Make a Table of 6 with Do-While Lopp.

*/

#include<iostream>
using namespace std;

int main()
{

    int a = 6;
    cout<<"The Table of 6 :\n";
    do
    {
        cout<<a<<endl;
        a++;
        a++;
        a++;
        a++;
        a++;
        a++;
    } while (a <= 60);

  return 0;  
}